    def timerFired(self, dt):
        if self.mode == "game":
            if self.over == True:
                self.init()
                self.over = False
            if self.pause == False:
                self.clock += dt
                if self.clock % 20 == 0:
                    if self.fast == True:
                        self.score += 5
                    else:
                        self.score += 1
                if self.clock % 1000 == 0:
                    self.probRect -= 0.1
                for arrow in self.arrowGroup:
                    if arrow.c == 0 and len(self.hit) != 0:
                        self.mode = "gameOver"
                    elif arrow.c == 1 and len(self.h) != 0:
                        self.mode = "gameOver"
                self.redTimerFired()
                self.arrowTimerFired()
                self.mapTimerFired()
                self.blueTimerFired(dt)
                self.powerUpTimerFired() 
    
    def redTimerFired(self):
        # physics reaction to all objects
        for red in self.redGroup:
            coll = pygame.sprite.spritecollideany(red, self.mapGroup)
            if coll != None:
                red.collision(0, 0, coll, "map")
            
            # react to arrow 
            coa = pygame.sprite.spritecollide(red, self.arrowGroup, \
            False)
            if len(coa) != 0:
                for arrow in self.arrowGroup:
                    red.collision(arrow.rect.midtop[0], arrow.rect.midtop[1], arrow, "arrow")    
            # react to blue 
            cob = pygame.sprite.spritecollideany(red, self.blueGroup)
            if cob != None:
                red.collision(cob.rect.center[0], cob.rect.center[1], cob, "obs")
                cob.collision(red.rect.center[0], red.rect.center[1], red, "obs1")
            
            # react to red
            cor = pygame.sprite.spritecollideany(red, self.redGroup)
            if cor != None and cor != red:
                red.collision(cor.rect.center[0], cor.rect.center[1], cor, "obs")
                cor.collision(red.rect.center[0], red.rect.center[1], red, "obs1")
                
                    
        if random.choice(self.obsType) == 0 and random.random() > self.probRect:
            self.redRectGroup.add(RedRectangle(self.width, self.height, self.red, self.fast))
        for arrow in self.arrowGroup:
            if arrow.c == 0:
                # detect collision for arrow kill
                self.hit.update(pygame.sprite.groupcollide(self.redGroup, self.arrowGroup, False, False))
                self.hit.update(pygame.sprite.groupcollide(self.redRectGroup, self.arrowGroup, False, False))
                
        if random.choice(self.obsType) == 1 and len(self.redGroup) < 5:
            r = RedSquare(self.width, self.height, self.red)
            b = pygame.sprite.spritecollideany(r, self.mapGroup)
            if b == None:
                self.redGroup.add(r)
        for red in self.redGroup:
            if red.rect.y > self.height + 20 or red.rect.y < -20:
                red.kill()
        self.redGroup.update(self.v)
        self.redRectGroup.update(self.v, self.fast)
    
    def blueTimerFired(self, dt):
        # physics reaction to all objects
        for blue in self.blueGroup:
            # react to map
            cb = pygame.sprite.spritecollideany(blue, self.mapGroup)
            if cb != None:
                blue.collision(0, 0, cb, "map")
            h = pygame.sprite.spritecollide(blue, self.arrowGroup, \
            False)
            
            # react to arrow 
            if len(h) != 0:
                for arrow in self.arrowGroup:
                    blue.collision(arrow.rect.midtop[0], arrow.rect.midtop[1], arrow, "arrow")
            
            # react to blue 
            cob = pygame.sprite.spritecollideany(blue, self.blueGroup)
            if cob != None and cob != blue:
                blue.collision(cob.rect.center[0], cob.rect.center[1], cob, "obs")
                cob.collision(blue.rect.center[0], blue.rect.center[1], blue, "obs1")
            
            # react to red
            cor = pygame.sprite.spritecollideany(blue, self.redGroup)
            if cor != None:
                blue.collision(cor.rect.center[0], cor.rect.center[1], cor, "obs")
                cor.collision(blue.rect.center[0], blue.rect.center[1], blue, "obs1")
        
        # detect collision for arrow kill
        for arrow in self.arrowGroup:
            if arrow.c == 1:
                # collision detect for blue object 
                self.h.update(pygame.sprite.groupcollide(self.blueGroup, self.arrowGroup, False, False))
        if self.clock % 100 == 0:
            num = 2
            for i in range(num):
                b = BlueSquare(self.width, self.height, self.blue)
                cm = pygame.sprite.spritecollideany(b, self.mapGroup)
                if cm == None:
                    self.blueGroup.add(b)
            for blue in self.blueGroup:
                if blue.rect.y > self.height + 20:
                    blue.kill()
                elif blue.rect.y < -20:
                    blue.kill()
        self.blueGroup.update(self.v)
        
        
    def powerUpTimerFired(self):
        if pygame.time.get_ticks() - self.fasttimer > poweruptime:
            self.fast = False
        if self.clock % 10 == 0:
            powerup = PowerUp(self.width, self.height, self.fast)
            c = pygame.sprite.spritecollideany(powerup, self.mapGroup)
            if c == None:
                self.powerupGroup.add(powerup)
        self.powerupGroup.update(self.v, self.fast)
        # collision detect for powerups 
        hits = pygame.sprite.groupcollide(self.powerupGroup, self.arrowGroup, True, False)
        for hit in hits:
            if hit.type == "fast":
                self.colorItem += 1
            if hit.type == "color":
                self.fastItem += 1
            if hit.type == "goldCoin":
                self.score += 10
    
    def arrowTimerFired(self):
        self.arrowGroup.update(self.isKeyPressed)
        
    def mapTimerFired(self):
        if self.fast == False:
            # map generation
            self.count += 1
            self.p = mapGen(self.p, self.count)
            
            # make sure arrow can pass
            if self.count > 10:
                self.count = 0 
            
            # clear each frame    
            self.mapGroup.empty()
            for r in range(self.r):
                for c in range(self.c):
                    if self.p[r][c] == 1:
                        x = self.s * c 
                        y = self.s * r
                        self.mapGroup.add(Map(self.red, x, y, self.s))
            for arrow in self.arrowGroup:
                if arrow.c == 0:
                    self.hit.update(pygame.sprite.groupcollide(self.mapGroup, self.arrowGroup, False, False))
        else:
            # map generation
            self.count += 1
            self.p = mapGen(self.p, self.count)
            self.p = mapGen(self.p, self.count)
            
            # make sure arrow can pass
            if self.count > 10:
                self.count = 0 
            
            # clear
            self.mapGroup.empty()
            for r in range(self.r):
                for c in range(self.c):
                    if self.p[r][c] == 1:
                        x = self.s * c 
                        y = self.s * r
                        self.mapGroup.add(Map(self.red, x, y, self.s))
            for arrow in self.arrowGroup:
                if arrow.c == 0:
                    self.hit.update(pygame.sprite.groupcollide(self.mapGroup, self.arrowGroup, False, False))
        
        
